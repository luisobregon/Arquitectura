    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

         Creararchivo_texto(); //El archivo de texto es creado en la carpeta registros del proyecto
        
        
        
        Conexion cc = new Conexion();
        Connection cn = cc.getConnection();
        String nombre, id, apellido,usuario,email;
        String sql = "";

        id = jTextField1.getText();
        usuario = jTextField2.getText();
        apellido = jTextField3.getText();
        nombre = jTextField4.getText();
        email = jTextField5.getText();

        sql = "INSERT INTO usuario (id,usuario,apellido,nombre,email) VALUES (?,?,?,?,?)";
        try {
            PreparedStatement psd = cn.prepareStatement(sql);
            psd.setString(1, id);
            psd.setString(2, usuario);
            psd.setString(3, apellido);
             psd.setString(4, nombre);
              psd.setString(5, email);
            int n = psd.executeUpdate();
            if (n > 0) {
                JOptionPane.showMessageDialog(null, "Usuario registrado");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
                jTextField5.setText("");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Logup.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

