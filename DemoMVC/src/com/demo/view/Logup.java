public void Creararchivo_texto(){
        
        String archivo=jTextField1.getText()+".registros";
        File crea_ubicacion = new File(ubicacion);
        File crea_archivo = new File(ubicacion+archivo);
        
      
            
           try {
                if(crea_archivo.exists()){
                JOptionPane.showMessageDialog(rootPane,"El registro ya existe");
                
                }else{
                
                crea_ubicacion.mkdirs();     // crea la carpeta
                Formatter crea= new Formatter(ubicacion+archivo);    // se crea el archivo
                crea.format("%s\r\n%s\r\n%s\r\n%s\r\n%s","ID="+jTextField1.getText(),"USUARIO="+jTextField2.getText(), 
                            "APELLIDOS="+jTextField3.getText(), "NOMBRES="+jTextField4.getText(),"EMAIL="+jTextField5.getText());
                crea.close();
                JOptionPane.showMessageDialog(rootPane, "Archivo de texto creado");
                
                } 
                
           } catch (Exception e){
               JOptionPane.showMessageDialog(rootPane, " ha fallado vuelva intentarlo");
           }
               
      
        }
        
        
    
    
    
